	public OCPBackground() {
		try {
			this.redSquare = ImageIO.read(new File("res/background/red.png"));
			this.purpleSquare = ImageIO.read(new File("res/background/purple.png"));
			this.bricks = ImageIO.read(new File("res/background/bricks.png"));
			this.lava = ImageIO.read(new File("res/background/lava.png"));
			this.barrierBricks = ImageIO.read(new File("res/background/barrierBricks.png"));
		}
		catch(IOException e) {
			
		}
		maxRows = map.length - 1;
    	maxCols = map[0].length - 1;
	}
	@Override
	public Tile getTile(int col, int row) {
		if (row < 0 || row > maxRows || col < 0 || col > maxCols) {
			image = null;
		}
		else if (map[row][col] == 0) {
			image = bricks;
		}
		else if (map[row][col] == 1) {
			image = barrierBricks;
		}
		else if (map[row][col] == 3) {
			image = lava;
		}
		
		int x = (col * TILE_WIDTH);
		int y = (row * TILE_HEIGHT);
		
		Tile newTile = new Tile(image, x, y, TILE_WIDTH, TILE_HEIGHT, false);
		
		return newTile;
	}

